			<h1>About Aaron</h1>
			<p>Aaron is a UK-born, Vancouver-bred songwriter and composer based Toronto. Infused with a DIY spirit, Aaron has been writing, recording, and releasing music since he was a teenager. He grew up in an eclectic pop music loving household, that was just as likely to be playing Cat Stevens as Duran Duran.  </p>

			<p>Starting as a self taught guitarist and songwriter while volunteering as the Production Manager at UBC's campus radio station, CITR, he released several college radio charting singles and EPs under various band names. After graduating from university, he studied Irish fiddle music before falling under the spell of John Zorn and the music coming out of the Tzadik label. He quickly found a mentor in classical and jazz guitar teacher Karlheinz Kiehlmann, whom he studied with for many years.</p>

			<p>Eventually, Aaron returned to the weird and eclectic music he made at CITR, recording and releasing two albums on UK label SVC Records in 2007 under the stage name Victor Scott. In 2008, the video for his song “Gotta Go” won the award for Best Video of the Year at the Canadian International Film Festival, and he independently released two more albums on his own Zygamatix label. </p>
		

			<p>After developing tintinitus while working on his fifth album, Aaron took some time off from music and moved to Toronto where he quickly become involved in the local scene, playing in original and cover bands as guitarist, drummer, bass player and vocalist. During this time, he resumed recording music by writing the theme song for his favourite podcast, Cooking Issues. This led to other gigs and he found himself getting offers to score film and video game projects.   </p>

			<p>He currently composes for film and TV, and plays in his post punk, country/surf/psych band the Tape Eagles. </p>
